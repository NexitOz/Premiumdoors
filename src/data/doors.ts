export function getDoorBySlug(slug: string): DoorModel | undefined {
  return DOORS.find((d) => d.slug === slug);
}

export function getRelatedDoors(door: DoorModel, count = 4): DoorModel[] {
  return DOORS.filter(
    (d) => d.slug !== door.slug && (d.collection === door.collection || d.system === door.system)
  ).slice(0, count);
}

